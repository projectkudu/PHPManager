        public static ConfigLocation GetChangableLocation(string setting)
        {
            ConfigLocation location;
            if (_settingLocations.TryGetValue(setting, out location))
            {
                return location;
            }
            else
            {
                return ConfigLocation.PHP_INI_SYSTEM;
            }
        }

